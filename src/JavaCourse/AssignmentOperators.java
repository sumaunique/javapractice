package JavaCourse;

public class AssignmentOperators {

	public static void main(String[] args) {
		int a = 10;
		a +=10;
		System.out.println(a);//20
		a -=5;
		System.out.println(a);//15
		a *=3;
		System.out.println(a);//45
		a /=3;
        System.out.println(a);//15
        a %=3;
        System.out.println(a);
	}

}
